#!/usr/bin/env node
// T2.5: reproducible eval report. Reads eval/summary.json, FAILS if any evidence path
// is missing (numbers may not outlive their sources), regenerates eval/REPORT.md.
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const summary = JSON.parse(readFileSync(join(root, 'eval', 'summary.json'), 'utf8'));

let missing = 0;
for (const row of summary.rows) {
  if (!existsSync(join(root, row.evidence))) {
    console.error(`MISSING EVIDENCE: ${row.evidence} (for "${row.metric}")`);
    missing++;
  }
}
if (missing) process.exit(1);

const lines = [
  '# Headroom eval report',
  '',
  `*Regenerated by \`npm run eval\` (last: ${summary.updated}). ${summary.note}*`,
  '',
  '| Suite | Metric | Naive | Headroom-equipped |',
  '|---|---|---|---|',
  ...summary.rows.map((r) => `| ${r.suite} | ${r.metric} | ${r.naive} | **${r.equipped}** | `),
  '',
  '## Honest notes (these are part of the results)',
  '',
  ...summary.honest_notes.map((n) => `- ${n}`),
  '',
  '## Evidence',
  '',
  ...[...new Set(summary.rows.map((r) => r.evidence))].map((e) => `- [\`${e}\`](../${e})`),
  '',
  'Methodology rules (artifact grading, demand-characteristics guards, publish-the-nulls): ADR-9 in `docs/DECISIONS.md`.',
];
writeFileSync(join(root, 'eval', 'REPORT.md'), lines.join('\n') + '\n');
console.log(`eval/REPORT.md regenerated — ${summary.rows.length} rows, all evidence present`);
